// =============================================================================
// Native OS window with embedded WebView for the NeuronPrompter frontend.
//
// Uses tao for window management (Win32/Cocoa/GTK) and wry for WebView
// embedding (WebView2 on Windows, WebKit on macOS/Linux). Provides a splash
// screen during server startup, then transitions to the main application
// window once the server is ready.
//
// Threading constraint: tao's event loop must run on the main thread
// (platform requirement on macOS and Windows). The axum HTTP server runs
// in a background thread with its own tokio runtime.
//
// This module is conditionally compiled behind the `gui` feature flag.
// =============================================================================

#![allow(unsafe_code)]

use std::rc::Rc;

use tao::dpi::LogicalSize;
use tao::event::{Event, WindowEvent};
use tao::event_loop::{ControlFlow, EventLoopBuilder, EventLoopProxy};
use tao::platform::run_return::EventLoopExtRunReturn;
use tao::window::{Icon, WindowBuilder};
use wry::WebViewBuilder;

/// Embedded 256x256 RGBA PNG of the NeuronPrompter "P" icon with the brand's
/// purple-to-cyan gradient. Pre-generated by tools/gen/generate_icon.py.
/// Decoded at runtime to raw RGBA pixels for tao's Icon::from_rgba().
/// Used on macOS and Linux where there is no embedded resource mechanism.
#[cfg(not(target_os = "windows"))]
const ICON_PNG: &[u8] = include_bytes!("../assets/icon_256.png");

/// Decodes the embedded icon PNG and creates a tao Icon from the raw RGBA
/// pixel data. Returns None if decoding fails. Used on macOS and Linux for
/// the window/dock icon. On Windows, the icon is loaded from the exe's
/// resource section instead (see `apply_resource_icon`).
#[cfg(not(target_os = "windows"))]
fn load_window_icon() -> Option<Icon> {
    let img = image::load_from_memory(ICON_PNG).ok()?.into_rgba8();
    let (w, h) = img.dimensions();
    Icon::from_rgba(img.into_raw(), w, h).ok()
}

/// On Windows, returns None because the icon is set after window creation
/// via `apply_resource_icon`, which loads the multi-resolution ICO from
/// the exe's resource section (embedded by winres at link time).
#[cfg(target_os = "windows")]
fn load_window_icon() -> Option<Icon> {
    None
}

/// Loads the multi-resolution ICO from the Windows executable's resource
/// section and sets it on the given window via Win32 `WM_SETICON` messages
/// for both the small icon (taskbar) and the big icon (Alt+Tab).
#[cfg(target_os = "windows")]
fn apply_resource_icon(window: &tao::window::Window) {
    use tao::platform::windows::WindowExtWindows;
    use windows::Win32::Foundation::{HWND, LPARAM, WPARAM};
    use windows::Win32::System::LibraryLoader::GetModuleHandleW;
    use windows::Win32::UI::WindowsAndMessaging::{
        GetSystemMetrics, IMAGE_ICON, LR_DEFAULTCOLOR, LoadImageW, SM_CXICON, SM_CXSMICON,
        SM_CYICON, SM_CYSMICON, SendMessageW, WM_SETICON,
    };
    use windows::core::PCWSTR;

    const ICON_SMALL: usize = 0;
    const ICON_BIG: usize = 1;

    // Resource ID 1 -- equivalent to MAKEINTRESOURCE(1).
    // The Win32 API encodes integer resource IDs as pointer values;
    // this is not a dangling pointer dereference.
    #[allow(clippy::manual_dangling_ptr)]
    let resource_id = PCWSTR(1 as *const u16);

    unsafe {
        let Ok(hinstance) = GetModuleHandleW(None) else {
            return;
        };

        let hwnd = HWND(window.hwnd() as *mut _);

        // Small icon for taskbar
        let small_width = GetSystemMetrics(SM_CXSMICON);
        let small_height = GetSystemMetrics(SM_CYSMICON);
        if let Ok(h) = LoadImageW(
            Some(hinstance.into()),
            resource_id,
            IMAGE_ICON,
            small_width,
            small_height,
            LR_DEFAULTCOLOR,
        ) {
            SendMessageW(
                hwnd,
                WM_SETICON,
                Some(WPARAM(ICON_SMALL)),
                Some(LPARAM(h.0 as isize)),
            );
        }

        // Big icon for Alt+Tab
        let large_width = GetSystemMetrics(SM_CXICON);
        let large_height = GetSystemMetrics(SM_CYICON);
        if let Ok(h) = LoadImageW(
            Some(hinstance.into()),
            resource_id,
            IMAGE_ICON,
            large_width,
            large_height,
            LR_DEFAULTCOLOR,
        ) {
            SendMessageW(
                hwnd,
                WM_SETICON,
                Some(WPARAM(ICON_BIG)),
                Some(LPARAM(h.0 as isize)),
            );
        }
    }
}

/// Background color matching `--bg-base: #0a0f1a` (fully opaque).
const MAIN_WINDOW_BG: (u8, u8, u8, u8) = (10, 15, 26, 255);

/// Custom user events for coordinating splash lifecycle, server readiness,
/// and window close actions on the tao event loop.
enum UserEvent {
    /// The server has bound its TCP port and is ready. Carries the URL.
    ServerReady(String),
    /// The server thread terminated during startup without providing a URL.
    ServerFailed,
    /// Frontend requested window close via IPC.
    CloseRequested,
}

// ---------------------------------------------------------------------------
// Linux Wayland compatibility
// ---------------------------------------------------------------------------

#[cfg(target_os = "linux")]
fn ensure_x11_backend() {
    if std::env::var_os("WAYLAND_DISPLAY").is_some() && std::env::var_os("GDK_BACKEND").is_none() {
        tracing::info!(
            "Wayland session detected; setting GDK_BACKEND=x11 for WebView compatibility"
        );
        // SAFETY: Called before any GTK or event-loop initialisation.
        unsafe {
            std::env::set_var("GDK_BACKEND", "x11");
        }
    }
}

#[cfg(not(target_os = "linux"))]
fn ensure_x11_backend() {}

// ---------------------------------------------------------------------------
// Pre-flight GUI availability check
// ---------------------------------------------------------------------------

/// Checks whether the native GUI can be created on the current platform.
///
/// # Errors
///
/// Returns a human-readable error string describing what is missing.
pub fn preflight_gui_check() -> Result<(), String> {
    #[cfg(target_os = "linux")]
    {
        let lib = std::ffi::CString::new("libwebkit2gtk-4.1.so.0")
            .map_err(|e| format!("CString error: {e}"))?;
        let handle = unsafe { libc::dlopen(lib.as_ptr(), libc::RTLD_LAZY) };
        if handle.is_null() {
            return Err(
                "WebKitGTK runtime library not found (libwebkit2gtk-4.1.so.0).\n\
                 Install with: sudo apt install libwebkit2gtk-4.1-0\n\
                 Or use headless mode: neuronprompter serve"
                    .into(),
            );
        }
        unsafe {
            libc::dlclose(handle);
        }
    }
    Ok(())
}

